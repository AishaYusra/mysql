import mysql.connector

class Student:
    def __init__(self, student_id, first_name, last_name, gpa):
        self.student_id = student_id
        self.first_name = first_name
        self.last_name = last_name
        self.gpa = gpa

   
    def connect_database():
        try:
            mydb = mysql.connector.connect(
                host="localhost",
                user="root",
                password="yusramysql21@",
                database="school"
            )
            return mydb
        except mysql.connector.Error as err:
            print(f"Error connecting to the database: {err}")
            return None

    def insert_student(self):
        db_connection = self.establish_db_connection()
        if db_connection:
            try:
                cursor = db_connection.cursor()
                cursor.execute("SELECT student_id FROM students WHERE student_id = %s", (self.student_id,))
                existing_student = cursor.fetchone()

                if existing_student:
                    print(f"Student with ID {self.student_id} already exists.")
                else:
                    cursor.execute(
                        "INSERT INTO students (student_id, first_name, last_name, gpa) VALUES (%s, %s, %s, %s)",
                        (self.student_id, self.first_name, self.last_name, self.gpa)
                    )
                    db_connection.commit()
                    print(f"Student with ID {self.student_id} added to the database.")
            except mysql.connector.Error as err:
                print(f"Error inserting student: {err}")
            finally:
                cursor.close()
                db_connection.close()

    def get_student_id(student_id):
        db_connection = Student.connect_database()
        if db_connection:
            try:
                cursor = db_connection.cursor(dictionary=True)
                cursor.execute("SELECT * FROM students WHERE student_id = %s", (student_id,))
                student = cursor.fetchone()
                if student:
                    return student
                else:
                    print(f"Student with ID {student_id} not found.")
                    return None
            except mysql.connector.Error as err:
                print(f"Error retrieving student: {err}")
            finally:
                cursor.close()
                db_connection.close()

    def update_gpa(self):
        db_connection = self.connect_database()
        if db_connection:
            try:
                cursor = db_connection.cursor()
                cursor.execute("UPDATE students SET gpa = %s WHERE student_id = %s", (self.gpa, self.student_id))
                if cursor.rowcount == 1:
                    db_connection.commit()
                    print(f"GPA updated for student with ID {self.student_id}.")
                else:
                    print(f"Student with ID {self.student_id} not found.")
            except mysql.connector.Error as err:
                print(f"Error updating GPA: {err}")
            finally:
                cursor.close()
                db_connection.close()

